                    <h4 class="mt-4"> How are Forex prices determined?</h4>
                    <p>Forex prices are driven by supply and demand in the market. 
                        When more people want to buy a currency, its value rises. When more want to sell, 
                        it falls. <strong>Key factors include:</strong>
                    </p>
                    <ol>
                        <li><strong>Interest Rates: </strong>Higher rates attract foreign capital, boosting currency value.</li>
                        <li><strong> Economic Indicators:</strong>GDP, inflation, employment data all impact currency strength.</li>
                        <li><strong>Political Stability:</strong>Stable governments attract investment; instability can weaken a currency.</li>
                        <li><strong>Market Sentiment:</strong>News, forecasts, and trader psychology can cause rapid price swings.</li>
                    </ol>
                    
                    <strong>For Example:</strong>
                    <p>Mr. Ebube starts trading the USD/JPY pair. If Japan's inflation rises while the U.S. economy strengthens, the Yen may weaken against the dollar. 
                        If demand for USD increases, the exchange rate might move from 50 to 200 yen per dollar. 
                        Different brokers may quote slightly different rates due to liquidity and spread differences</p>

                    <h4 class="mt-5">Key factors influencing Forex price</h4>
                    <ol>
                        <li><strong>Supply and Demand:</strong>The core driver of all price movements.</li> 
                        <li><strong>Central Bank Policies:</strong>Interest rate hikes or cuts can strengthen orweaken a currency.</li> 
                        <li><strong>Liquidity:</strong> Major pairs have tighter spreads and more stable pricing.</li> 
                        <li><strong>Geopolitical Events: </strong>Elections, wars, or trade agreements can cause volatility.</li> 
                        <li><strong>Economic Reports:</strong> Non-farm payrolls, CPI, and GDP releases often move markets. </li> 
                    </ol>

